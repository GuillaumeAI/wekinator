    private void doneButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doneButtonActionPerformed
        // TODO add your handling code here:
        if(currentSaveLocation.equals("None"))
        {
            Util.showPrettyErrorPane(this, "Must set save location");
            return;
        }
        File f = new File(currentSaveLocation);
        if (!f.exists()) {
            try {
                f.mkdirs();
            } catch (Exception ex) {
                Util.showPrettyErrorPane(this, "Could not create directory " + currentSaveLocation + ". Please choose a different location.");
                return;
            }
        }
        GlobalSettings s = GlobalSettings.getInstance();
        try {
            s.setKadenzeSaveLocation(f.getCanonicalPath());
        } catch (IOException ex) {
            s.setKadenzeSaveLocation(f.getAbsolutePath());
        }
        try{
            KadenzeLogging.startLoggingForAssignment(KadenzeAssignment.KadenzeAssignmentType.FEATURNATOR_STUDY_2);
        } catch (IOException e) {
            
        }
        try{
            WekiMiniRunner runner = WekiMiniRunner.getInstance();
            if(studyID == StudyID.STUDY_3) 
            {
                w = runner.runStudy3(currentSaveLocation);
            }
            else
            {
                w = runner.runStudy2(currentSaveLocation);
                w.getMainGUI().setVisible(true);
                runner.transferControl(w, this, w.getMainGUI());
            }
        } catch (Exception e){
            e.printStackTrace();
        }
        this.dispose();
    }//GEN-LAST:event_doneButtonActionPerformed

    private void saveLocationButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveLocationButtonActionPerformed
        // TODO add your handling code here:
        JFileChooser fc = new JFileChooser(currentSaveLocation) {

        };
        fc.setDialogType(JFileChooser.SAVE_DIALOG);
        fc.setApproveButtonText("Choose directory");
        fc.setDialogTitle("Choose directory for Week 6 Assignment");
        fc.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);

        int returnVal = fc.showSaveDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File chosenFile;
            File f = fc.getSelectedFile();
            if (f.isDirectory()) {
                chosenFile = f;
            } else {
                chosenFile = f.getParentFile();
            }
            try {
                currentSaveLocation = chosenFile.getCanonicalPath();
            } catch (IOException ex) {
                //Rare event that can't resolve canonical path name
                currentSaveLocation = chosenFile.getAbsolutePath();
            }
            DateFormat dateFormat = new SimpleDateFormat("ddMMyy-HHmm");
            Date date = new Date();
            currentSaveLocation = currentSaveLocation + File.separator + "Featurnator-"+dateFormat.format(date);
            filePathLabel.setText("<html>"+currentSaveLocation);
        }
    }//GEN-LAST:event_saveLocationButtonActionPerformed

    private void openButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openButtonActionPerformed
        // TODO add your handling code here:
        String lastLocation = GlobalSettings.getInstance().getStringValue("wekinatorProjectLoadLocation", "");
        if (lastLocation.equals("")) {
            lastLocation = System.getProperty("user.home");
        }

        File f = Util.findLoadFile(WekinatorFileData.FILENAME_EXTENSION, "Wekinator file", lastLocation, this);
        if (f != null) {
            try {
                File parentDir = new File(lastLocation);
                GlobalSettings s = GlobalSettings.getInstance();
                try {
                    s.setKadenzeSaveLocation(parentDir.getCanonicalPath());
                } catch (IOException ex) {
                    s.setKadenzeSaveLocation(parentDir.getAbsolutePath());
                }
                try{
                    KadenzeLogging.startLoggingForAssignment(KadenzeAssignment.KadenzeAssignmentType.FEATURNATOR_STUDY_2);
                } catch (IOException e) {

                }
                WekiMiniRunner.getInstance().runFromFile(f.getAbsolutePath(), true);
                this.dispose();
            } catch (Exception ex) {
                Logger.getLogger(MainGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_openButtonActionPerformed

