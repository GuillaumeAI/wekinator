    public void setCanCancelAuto(boolean canCancel)
    {
        this.canCancelAuto = canCancel;
        if(canCancel)
        {
            autoButton.setEnabled(true);
            autoButton.setText("Cancel");
        }
        else
        {
            autoButton.setEnabled(true);
            autoButton.setText("Auto Select");
        }
        
    }
    
