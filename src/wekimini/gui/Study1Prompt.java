    private void startButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startButtonActionPerformed
        // TODO add your handling code here:
        File f = new File(currentSaveLocation + File.separator + idTextField.getText());
        //File f = new File(currentSaveLocation);
        if (!f.exists()) {
            try {
                f.mkdirs();
            } catch (Exception ex) {
                Util.showPrettyErrorPane(this, "Could not create directory " + currentSaveLocation + ". Please choose a different location.");
                return;
            }
        }
        GlobalSettings s = GlobalSettings.getInstance();
        try {
            s.setKadenzeSaveLocation(f.getCanonicalPath());
        } catch (IOException ex) {
            s.setKadenzeSaveLocation(f.getAbsolutePath());
        }
        try{
            KadenzeLogging.startLoggingForAssignment(KadenzeAssignment.KadenzeAssignmentType.FEATURNATOR_STUDY_1);
        } catch (IOException e) {
            
        }
        try{
            setupProject();
            WekiMiniRunner.getInstance().runStudy1();
        } catch (Exception e){}
        this.dispose();
    }//GEN-LAST:event_startButtonActionPerformed

    public void setupProject()
    {
        String dir = ((FeaturnatorLogger)KadenzeLogging.getLogger()).getUserDir();
        File project = new File(dir + "Study1");
        File template = new File("/Users/louismccallum/Documents/Goldsmiths/Wekinator_Projects/Wekinator_Projects/Study1/");
        if(!project.exists())
        {
            try{
                FileUtils.copyDirectory(template, project);
            } catch (IOException e)
            {
                System.out.println(e.getLocalizedMessage());
            }
        }
    }
    
